#include <stdio.h>
int main() {
 printf("Integer equivalents of some uppercase letters:\n");
 printf("A: %d\n", 'A');
 printf("B: %d\n", 'B');
 printf("C: %d\n", 'C');
 printf("Integer equivalents of some lowercase letters:\n");
 printf("a: %d\n", 'a');
 printf("b: %d\n", 'b');
 printf("c: %d\n", 'c');
 printf("Integer equivalents of some digits:\n");
 printf("0: %d\n", '0');
 printf("1: %d\n", '1');
 printf("2: %d\n", '2');
 printf("Integer equivalents of some special symbols:\n");
 printf("$: %d\n", '$');
 printf("*: %d\n", '*');
 printf("+: %d\n", '+');
 printf("/: %d\n", '/');
 printf("Blank character: %d\n", ' ');
 return 0;
}
